public class Sum
{
    public static void main(String args[])
    {
        int a,b,c;
        double Sum,Average;
        a=200;b=350;c=450;
        Sum = a+b+c;
        Average = (a+b+c)/3;
        System.out.println("The sum of the three numbers is: " +Sum);
        System.out.println("The average of the three numbers is: " +Average);
    }
}
